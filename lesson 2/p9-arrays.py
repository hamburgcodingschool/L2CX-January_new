# ARRAY
# an array is a dataset that can contain many values

names = ["Helder", "Fabia", "Linda", "Leonie", "Carina", "Ivan", "Lexy"]

print(names)

names[5] = "Joe"

print(names)

names.remove("Fabia")

print(names)

names.append("New person")

print(names)

names.insert(0, "ABC")

print(names)

names.insert(2, "This will be the 3rd")

print(names)

names.pop(4)

print(names)